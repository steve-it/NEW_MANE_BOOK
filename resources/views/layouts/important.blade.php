                            <h3>L'origine de la création de l’École Camerounaise d'Administration</h3>
                            </p>
                            <p>
                                Le nom d'origine à la création est l’École Camerounaise d'Administration (ECA).
                                Cette école constitue le principal creuset des administrateurs de la fonction publique
                                camerounaise depuis l'indépendance du Cameroun en date du 1er mai 1960.
                                En 1961, l'école ouvre un cycle spécial pour préparation au concours d'entrée aux cycles
                                normaux des candidats issus des régions sous-scolarisées.
                                Le Cameroun possède deux langues nationales officielles depuis son indépendance et sa
                                réunification. De ce fait, en 1963 un cycle préparatoire au concours d'entrée au cycle A
                                et B, pour les candidats anglophones voit le jour.
                                Le changement majeur intervient en 1964: L’École Camerounaise d'Administration (ECA)
                                devient l’École Normale d'Administration et de la Magistrature (ENAM)4. La mission
                                principale aussi change. Il s'agit maintenant de former le personnel civil et financier
                                de l’État, ainsi que les magistrats du judiciaire.
                                En 1985, l'ENAM fait partie du Centre National d'Administration et de la Magistrature
                                (CENAM) avec l'Institut Supérieur de Management Public (ISMP) et l'Institut des
                                Techniques Administratives et Financières (ITAF). 1995 marque la réouverture des cycles
                                A et B pour la formation du personnel du service civil et financier.
                                Cette année marque aussi la création du Centre de Recherche et de la Documentation pour
                                les problèmes concernant l'administration. 2010 marque l'ouverture de l'admission à la
                                division judiciaire aux diplômés de l'informatique et en sciences économiques de
                                l'enseignement supérieur. Depuis 2012, les diplômés en informatique et sciences
                                économiques de l'enseignement supérieur concourent à l'admission à la division de la
                                magistrature et des greffes.
